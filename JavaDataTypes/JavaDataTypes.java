package JavaDataTypes;

public class JavaDataTypes {
    public static void main(String[] args) {
        int myNum = 5; // Stores Integer values (whole number)
        float muFloatNum = 5.99f; // Stores Floating point number
        char myLetter = 'D'; // Stores a single character
        boolean myBool = true; // Stores boolean values true/false
        String myText = "Hello"; // Stores text values

        System.out.println("Stores Integer values (whole number): " + myNum);
        System.out.println("Stores Floating point number: " + muFloatNum);
        System.out.println("Stores a single character: " + myLetter);
        System.out.println("Stores boolean values true/false: " + myBool);
        System.out.println("Stores text values: " + myText);
    }
}
