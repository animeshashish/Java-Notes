package JavaDataTypes;

public class IntDataTypes {
    public static void main(String[] args) {

        // Byte Data Type: The byte data type can store whole numbers from -128 to 127.
        byte myNum = 100;
        System.out.println("The byte data type can store whole numbers from -128 to 127 = " + myNum);

        // Short Data Type: The short data type can store whole numbers from -32768 to 32767:
        short myNum2 = 5000;
        System.out.println("The short data type can store whole numbers from -32768 to 32767 = " + myNum2);

        // Int Data Type: The int data type can store whole numbers from -2147483648 to 2147483647.
        int myNum3 = 100000;
        System.out.println("The int data type can store whole numbers from -2147483648 to 2147483647 = " + myNum3);

        // Long Data Type: The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807.
        // Note that you should end the value with an "L"
        long myNum4 = 15000000000L;
        System.out.println("The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807 = " + myNum4);
    }
}
