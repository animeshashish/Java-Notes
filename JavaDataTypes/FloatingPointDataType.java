package JavaDataTypes;

public class FloatingPointDataType {
    public static void main(String[] args) {

        // Floating Point Types: The floating point type is used whenever you need a number with a decimal, such as 9.99 or 3.14515.
        // Note that you should end the value with an "f" for floats.
        float myNum = 5.75f;
        System.out.println("The Double data type is used whenever you need decimal after one numbers = " + myNum);

        // Double Data type: The Double data type is used whenever you need decimal after two numbers.
        // Note that you should end the value with an "d" for floats.
        double myNum2 = 19.99d;
        System.out.println("The Double data type is used whenever you need decimal after two numbers = " + myNum2);
    }
}
